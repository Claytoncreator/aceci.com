<h1>Projet Environnement - Développement durable</h1>
<img src="assets/images/env.png">
<p>
	La question de l’environnement se trouve au cœur de l’éthique de la coopération eu égard aux changements climatiques et à leurs impacts sur les vies humaines au Nord comme au Sud mais surtout sur les plus vulnérables de la planète. En plus d’informer sur ce sujet, l’ACECI vient de mettre sur pieds un projet en matière de crédits de carbone et d’échanges Nord/Sud.
</p>
<h1>Mise en marché des crédits éthiques de carbone</h1>
<img src="assets/images/sky.png">
<p>
	Ce projet est né suite aux faits et constats suivants :<br><br>

​
<ol><li><br>
    Les difficultés de nombreux intervenants des pays du Sud dans le domaine de la réduction des gaz à effet de serre (GES) de mettre en valeur les crédits potentiels de ces réductions, de les faire enregistrer et de les vendre sur un marché lucratif. Un bilan récent fait état d’une participation de l’Afrique à la hauteur de 3% dans la présentation de projets sur le marché des crédits de carbone. Un bilan<br> très faible puisqu’il s’agit du continent au plus haut rythme de désertification et extrêmement vulnérable aux changements climatiques. Selon plusieurs experts, les pays du Sud manquent<br> de support technique.
     </li><br>

   <li> Lors de la 2ème édition des grandes conférences diplomatiques Nord/Sud organisée par l’ACECI en 2008 sur le cas des forêts équatoriales (Cameroun, Gabon, RDC) dans la gestion des ressources naturelles, il est ressorti que les intervenants du Sud manquaient d’informations et d’expertises relatives au marché des crédits de carbone.</li><br>
     

   <li> Les difficultés des clients et acheteurs potentiels de crédit de carbone d'évaluer l’effet réducteur de carbone, d'en évaluer la quantité.</li><br>
     

   <li> Les difficultés d’évaluer la valeur ajoutée de ces échanges sur les conditions de vie des populations.
</li></ol>
 <br><br>

Responsable de projet: M. André Moffet, Ingénieur forestier.<br><br>

