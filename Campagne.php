<h1>Textes de référence</h1>
La campagne se base sur toute une série de textes de référence, en commençant par la Déclaration Universelle des Droits de l'Homme.

La réalisation des Objectifs du Millénaire pour le Développement est un des objectifs de la loi visant la réduction de la pauvreté.
