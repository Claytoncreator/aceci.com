<h1>Projet Cataire – Lutte contre le paludisme dans une vision du développement durable</h1>
En 2010, une délégation de l’ACECI a soumis au gouvernement du Burundi, un projet de production des produits anti-moustiques dans une vision du développement durable.Le projet a été reçu avec enthousiasme. Son lancement par le Président de la République du Burundi a eu lieu le 29 avril 2011 à Ngozi  - Photo avec le Président Nkurunziza - après la conférence scientifique du 13 au 16 octobre 2010 à Bujumbura. 
</div>
<img src="assets/images/cool.png" class="img">
<div class="monde">
	Ailleurs dans le monde, les piqûres de moustiques dérangent, incommodent et causent des allégies. En Afrique et dans les pays pauvres, les piqûres de moustique – celles de l’anophèle causent la malaria et entraînent la mort.

    <ol><li>Entre 350 et 500 millions de cas sont diagnostiqués chaque année</li>

    <li>Plus de  5 000 personnes meurent par jour.</li>

    <li>La malaria affecte les populations les plus pauvres.</li>

   <li> Les enfants et les femmes enceintes sont des groupes vulnérables.

    90% des décès sont enregistrés en Afrique sub-saharienne.</li></ol>

Par ses effets, la malaria provoque aussi un cycle infernal de la pauvreté entravant le développement économique.

Au Burundi comme dans la plus part des pays subsahariens, la malaria est la première cause de mortalité. C’est pourquoi, pour l’ACECI, la lutte contre le paludisme est une priorité. Un préalable pour le développement durable.

</div>
<br><br>
<p>
	Les actions visent à :<br>
Investir dans la fabrication et la promotion des anti-moustiques à <br>base des plantes en Afrique, par et pour les africains grâce au transfert des technologies, c’est :<br>

    <ol><li>investir dans les populations</li>

   <li> stimuler la croissance économique</li>

    <li>Viser le développement durable</li>

    <li>Protéger la population contre les moustiques, vecteurs de la malaria</li>

    <li>Garantir l’accès aux produits anti-moustiques – seul vecteur de la malaria par des mesures législatives</li>

    <li>Sensibiliser et travailler en faveur la sécurité alimentaire car la malnutrition aggrave les cas de paludisme.​</li></ol>


</p>
