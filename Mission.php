<h1><u>VISION,MISSION ET OBJECTIFS</u></h1>
<h2>Vision</h2>
<p>Un partenariat mondial responsable, une coopération équilibrée et un développement éclairé.</p>
<h2>Mission</h2>
<p>ACECI plaide en faveur du facteur humain dans les grands enjeux mondiaux comme la lutte contre la pauvreté, la gestion de l’environnement, le développement durable et les relations bi- et ultilatérales.</p>
<h2>Baseline</h2>
<p>Au rendez-vous du donner et du recevoir.</p>
<h2>Objectifs</h2>
<p>

   <li> Œuvrer pour une nouvelle configuration de la coopération basée sur le modèle de partenariat responsable</li>

    <li>Promouvoir l'institutionnalisation de l'éthique de la coopération</li>

    <li>Sensibiliser les intervenants et décideurs du Nord et du Sud à la prise en compte du facteur humain dans les programmes d’envergure nationale et internationale dans une perspective globale du développement éclairé.</li>

    <li> le principe du donner et du recevoir dans les relations d'affaires et de coopération entre le Nord et le Sud dans le respect du partenariat responsable</li>

   <li> Promouvoir le développement humain minimum garanti (DHMG)</li>
 </p>
<h2>Intervention par 5 axes</h2>
<p>

  <li>information </li>

   <li> formation</li>

    <li>études de terrain</li>

    <li>recherche</li>

   <li> consultances</li></ol>
</p>
<h2>Projets principaux</h2>
  <p> <li> Traduire les OMD en une loi-type visant la réduction de la pauvreté</li>

   <li> Programme de stages au Nord comme au Sud</li>

   <li> Programmes de plaidoyer</li>
</p>
<h2>A qui s'adresse ACECI ?</h2>
<p>
	En éthique des affaires au plan international, au cours de la dernière décennie, on est passé peu à peu d’un regard axé sur la corruption des dirigeants politiques des pays du Sud à une analyse des modes d’intervention des grandes entreprises dans ces pays et à une attention à la diversité culturelle des territoires dans lesquelles elles s’inscrivent. Mais, il n’est pas rare que l’on cherche encore souvent à imposer des façons de voir et de faire qui ne tiennent pas compte de l'éthique de la coopération (reconnaissance, responsabilité sociale, bienfaisance, souveraineté, etc.), des conditions de vie, des traditions et des symboliques des populations. On tend à fixer des normes qui viennent des pratiques du Nord et qui sont le fruit d’une autre expérience. Dans ces cas, la démarche éthique ne repose pas sur une réflexion partagée et une recherche collective des conduites adéquates. Elle ne peut alors porter les bienfaits escomptés et contribuer à la prise en charge par les uns et les autres d’une coopération efficace.</p>

 
<p>
<h3>C’est pourquoi, ACECI s’adresse notamment aux:</h3>

 - instances gouvernementales des pays du Nord et du Sud.<br>
 - organisations internationales.<br>
 - institutions internationales. <br>
 - fondations caritatives. <br>
 - entreprises privées du Nord faisant affaires avec le Sud.<br>
 - entreprises privées et publiques du Sud.<br><br>
</p>
