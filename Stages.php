	STAGES INTERNATIONAUX

Organisme québécois à caractère international, l’ACECI innove par sa vision et sa mission axées sur la promotion de l’éthique de la coopération dans les rapports Nord-Sud.
<br><br><br>
​

Les stages offerts par l’ACECI s’articulent autour d’axes d’intervention en études de terrain, information, formation, recherche, et en consultance au siège, à Québec et auprès de ses partenaires, particulièrement en Afrique. De fait, après le succès des stages internationaux effectués au Burundi en 2008 et au Togo en 2010, l’ACECI entend consolider son programme de stages diversifiés.
<br><br><br>
​

Un stage en éthique de la coopération offre l'opportunité de

​<br><br>

   <ul><li> Acquérir une expérience internationale dans une logique de partenariat responsable avec différents collaborateurs</li>

    <li>Un enrichissement et une ouverture en matière de coopération entre le Nord et le Sud</li>

    <li>Renforcer ses connaissances en éthique de la coopération Nord-Sud</li>

   <li> Participer à la réalisation des projets innovateurs</li>

   <li> Se préparer à une carrière à l’international</li></ul>

 

