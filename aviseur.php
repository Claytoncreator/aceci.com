<h1>Aminou SAKA :</h1>
Lorsqu’il fonde SERT en 1979, Saka Aminou avait uniquement à cœur de bâtir une passerelle entre le Nord et le Sud. Plus de 20 ans après, il a réussi au delà de toute espérance. Aminou Saka a non seulement mis sur pied une entreprise solide, mais il a également pris une large part au recul du sous-développement en Afrique. Homme d’une double culture, africaine et québécoise, il réussit à tisser des liens étroits et multiples entre la Belle Province et le Continent Noir. En véritable maestro, entouré d’une équipe dévouée et compétente, Aminou Saka a fait de SERT un passage obligé pour les échanges entre l’Afrique et le Québec. Les nombreux projets qu’il a initiés et menés à terme avec un remarquable succès en font un interlocuteur privilégié aussi bien pour les hommes d’affaires québécois que pour les communautés africaines. Il n’est pas seulement le Président d’une compagnie d’envergure, mais une courroie de transmission entre deux mondes qui se sont longtemps ignorés. À sa façon Aminou Saka est un visionnaire et un pionnier qui a défriché tant de chemins et de routes, au grand bénéfice de l’Afrique et du Québec.</div>

