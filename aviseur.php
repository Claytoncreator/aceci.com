<div class="Jacques">
<h1>Jacques RACINE</h1>
 Jacques Racine est professeur à la Faculté de théologie et de sciences religieuses de l’Université Laval depuis 1970. Il a été doyen de la Faculté de 1982 à 1989. Vice-recteur exécutif de l’Université Laval de 1989 à 1998, il était alors responsable de la planification et des finances et il présidait le Comité de placement du Fonds commun des régimes de retraite de l’Université. De retour à l’enseignement, il est responsable de cours en éthique sociale et politique et de cours en éthique des affaires au M.B.A. Il a participé à divers conseils d’administration dans le monde de l’éducation et des services hospitaliers. Il a aussi été membre du Conseil supérieur de l’éducation et président de la Commission de l’enseignement supérieur. Il préside le Comité d’éthique du Fonds commun de placement de l’Université Laval. Il est auteur ou coauteur de nombreux articles et livres.</div>
