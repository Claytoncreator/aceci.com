	<h1>Ernest MBONDA</h1>
Est professeur de philosophie à l’Université catholique d’Afrique centrale à Yaoundé au Cameroun depuis 1996. Ses enseignements portent sur la philosophie morale, la philosophie sociale et politique, la philosophie du droit, l’éthique de la solidarité internationale et l’action humanitaire. Il fait partie du comité de réseau de l’Agence universitaire de la francophonie « État de droit saisi par la philosophie » et est membre du comité scientifique de la revue « Aspects » (de l’État de droit) de l’AUF. Il a fondé en 2002, au sein de l’Université catholique d’Afrique centrale, le Centre d’Etudes et de recherches sur la justice sociale et politique. Il participe également aux différents projets élaborés par l’Association pour la promotion des droits de l’homme en Afrique centrale.

