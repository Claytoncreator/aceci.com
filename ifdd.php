Signature d'un accord- cadre de collaboration entre l´Institut de la Francophonie pour le développement durable (IFDD) et l´ACECI

 L’IFDD a signé le 27 mars 2013 un accord - cadre de collaboration avec l’Agence consultative en éthique de la coopération internationale (ACECI). Cet accord porte sur le développement conjoint et la mise en œuvre de programmes de renforcement des capacités dans l’espace francophone. Concrètement, les deux parties pourront œuvrer conjointement pour la réalisation des actions suivantes :

    Développement de programmes conjoints de renforcement des capacités des catégories d’acteurs en développement durable;

    Collaboration pour la conduite d’actions de sensibilisation, d’information et de plaidoyer en faveur du développement durable dans l’espace francophone;

    Développement et diffusion d’outils;

    Partage d’information et diffusion de bonnes pratiques d’élimination de la pauvreté pour le développement durable;

    Développement et mise en œuvre de projets pilotes favorisant l’élimination de la pauvreté dans le contexte du développement durable;

    Contribution à des plateformes d’échanges de savoirs et de partage d’expériences sur les thématiques du champ de collaboration;

    Recherche et mobilisation de financement pour la mise en œuvre des actions conjointement retenues.
