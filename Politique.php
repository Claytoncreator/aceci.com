<div><h1>Politique anti-corruption de l’ACECI</h1>

 

<h3>Politique</h3>

    <li> L’ACECI s’engage à lutter contre la corruption en établissant des normes de conduite à l’intérieur de ses codes de principe.</li>

<h3>But</h3>
<ul><li>Cette politique vise à diriger les décisions et les actions des agents, des directeurs, des membres,<br> des employés, des membres des comités, des universitaires et des autres bénéficiaires de l’organisme.</li></ul>

<h3>Principes</h3>
    <li>Tous les administrateurs, employés, membres et partenaires de l’ACECI partagent un commun intérêt<br> de lutter contre la corruption qui porte atteinte à la bonne gestion des affaires publiques ainsi qu’à une<br> utilisation appropriée des ressources destinées au développement et compromet une concurrence<br> transparente et ouverte de l’utilisation et de la distribution des ressources dont dispose l’ACECI.<br> Ils déclarent en conséquence joindre leurs efforts pour lutter contre la corruption et qu’aucune offre,<br> aucun don ou paiement, aucune rémunération ou avantage d’aucune sorte considéré comme un acte<br> illicite ou une pratique de corruption n’a été ou ne sera accordé à qui que ce soit, directement <br>ou indirectement, en vue ou en contrepartie de l’attribution ou de l’exécution de la présente politique.</li>
<br><br>
Cette Politique a été adoptée par Résolution à l’unanimité lors de la séance du Conseil d’Administration de l’ACECI, le 11 mars 2011
