	<h1>EQUIPE ALPHA</h1>

<h2>Contexte</h2>

​

Le facteur humain étant le critère éthique par excellence dans les rapports Nord/Sud, l’ACECI a choisi de faire de la lutte contre la pauvreté un enjeu capital. C'est pourquoi l'équipe Alpha a été mise sur pieds après la première édition des grandes conférences diplomatiques consacrée à la déclaration de Paris et la gestion de la coopération Nord-Sud.

​

<h3>Sa mission et objectifs</h3>

​

- Donner une suite opérationnelle à la première édition des GCDNS<br>
- Traduire en projets et programmes la mission de l’ACECI en matière de la lutte contre la pauvreté.<br>
- Promouvoir la vision éthique d’aide au développement<br>
- Concevoir un plan d’action à la lumière du rapport et documents de synthèse de la première édition des GCDNS et selon les axes d’intervention de l’ACECI.<br>
- Identifier les partenaires intéressés par l’éthique de la coopération.<br>

​

<h3>Projet principal</h3>

​

Oeuvrer avec les acteurs nationaux, régionaux et internationaux afin de traduire les objectifs du millénaire en une loi anti-pauvreté dans les pays du Sud.

<h3>Projets à court terme :</h3>

- Préparer la 3ème édition des GCDNS.<br>
- Participer au processus post-Accra sur la Déclaration de Paris<br>
- Préparer le premier volume du Guide sur l’éthique de la coopération<br>


<h3>Membres</h3>

​

Sous la coordination de la direction de l’ACECI, l’équipe Alpha regroupe les chercheurs, experts et professionnels interdisciplinaires.

